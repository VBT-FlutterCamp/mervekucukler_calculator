class Islemler {
  void Topla(double sayi1, double sayi2) {
    double sonuc = sayi1 + sayi2;
    print("Toplama İşleminin Sonucu: ${sonuc}");
  }

  void Cikar(double sayi1, double sayi2) {
    double sonuc = sayi1 - sayi2;
    print("Çıkarma İşleminin Sonucu: ${sonuc}");
  }

  void Carpma(double sayi1, double sayi2) {
    double sonuc = sayi1 * sayi2;
    print("Çarpma İşleminin Sonucu: ${sonuc}");
  }

  void Bolme(double sayi1, double sayi2) {
    double sonuc = sayi1 / sayi2;
    print("Bölme İşleminin Sonucu: ${sonuc}");
  }
}
